<script lang="ts">
  import { counter } from "$stores/store";
  import { getStores } from "$app/stores";
  const session = getStores().session;
  session.update((val) => {
    let sess = { ...(val || {}) };
    if (!sess) {
      sess = {};
    }
    sess.counter = 0;
    return sess;
  });
  const increment = () => {
    let newVal = 0;
    counter.update((value) => {
      newVal = value + 1;
      return newVal;
    });
    session.update((val) => {
      val.counter = newVal;
      return val;
    });
  };
</script>

<button on:click={increment}>Clicks: {$counter}</button>

